package com.cc.jcg.tools.xml;

public class XmlVisitorException
	extends Exception {

    private static final long serialVersionUID = 1L;

    public XmlVisitorException() {
	super();
    }

    public XmlVisitorException(String message, Throwable cause) {
	super(message, cause);
    }

    public XmlVisitorException(String message) {
	super(message);
    }

    public XmlVisitorException(Throwable cause) {
	super(cause);
    }
}
